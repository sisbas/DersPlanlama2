export function buildBlockSlots(
  day: string,
  startPeriodNo: number,
  blockHours: number,
  config: SchoolScheduleConfig
): string[] | undefined {
  const activeCount = getActivePeriodsCountForDay(day, config);
  const slots: string[] = [];
  let periodNo = startPeriodNo;

  while (slots.length < blockHours) {
    if (periodNo > activeCount) return undefined;
    if (config.ogleArasiPeriyotNo !== undefined && periodNo === config.ogleArasiPeriyotNo) {
      periodNo++;
      continue;
    }
    slots.push(`${day}-${periodNo}`);
    periodNo++;
  }

  return slots;
}

function makeAutoPlan(
  sinif: ClassUnit,
  course: Course,
  teacher: Teacher,
  room: Classroom,
  slot: string
): PlanItem {
  return {
    id: `${AUTO_PLAN_ID_PREFIX}${Date.now()}-${Math.random().toString(36).substring(2, 8)}`,
    planTuru: course.brans === "Rehberlik" ? PlanTuru.REHBERLIK : PlanTuru.NORMAL_DERS,
    durum: "Taslak",
    sinifId: sinif.id,
    dersId: course.id,
    ogretmenId: teacher.id,
    derslikId: room.id,
    periyotId: slot,
  };
}

export function normalizeSchedulingInput(
  plans: PlanItem[],
  teachers: Teacher[],
  classes: ClassUnit[],
  classrooms: Classroom[],
  courses: Course[],
  config?: SchoolScheduleConfig
): EffectiveSchedulingInput {
  const finalConfig: SchoolScheduleConfig = config || {
    activeDays: ["Pazartesi", "Salı", "Çarşamba", "Perşembe", "Cuma", "Cumartesi", "Pazar"],
    longDays: ["Salı", "Perşembe", "Cuma"],
    longDaysHours: { start: "08:30", end: "20:00" },
    regularDaysHours: { start: "08:30", end: "17:00" },
    lessonDuration: 40,
    recessDuration: 10,
    ogleArasiPeriyotNo: 5
  };
  const effectiveAllowedPeriods = new Map<string, string[]>();
  const effectiveAvailablePeriods = new Map<string, string[]>();

  const activePeriodsList: string[] = [];
  const activeCount = Math.max(14, getMasterTimeSlots(finalConfig).length);
  finalConfig.activeDays.forEach(day => {
    for (let pNo = 1; pNo <= activeCount; pNo++) {
      if (finalConfig.ogleArasiPeriyotNo && pNo === finalConfig.ogleArasiPeriyotNo) continue;
      activePeriodsList.push(`${day}-${pNo}`);
    }
  });

  classes.forEach(cls => {
    let allowed: string[] = [];
    if (cls.uygunPeriyotlar && cls.uygunPeriyotlar.length > 0) {
      allowed = [...cls.uygunPeriyotlar];
    } else {
      const policy = getLevelPolicy(cls);
      if (policy && policy.hardWindows && policy.hardWindows.length > 0) {
        allowed = activePeriodsList.filter(slot => {
          const day = getDay(slot);
          const pNo = getPeriodNo(slot);
          return policy.hardWindows!.some(window => windowMatches(window, day, pNo, activeCount));
        });
      } else {
        allowed = [...activePeriodsList];
      }
    }
    effectiveAllowedPeriods.set(cls.id, allowed);
  });

  teachers.forEach(teacher => {
    let available: string[] = [];
    if (teacher.uygunPeriyotlar && teacher.uygunPeriyotlar.length > 0) {
      available = [...teacher.uygunPeriyotlar];
    } else {
      available = activePeriodsList.filter(slot => {
        const day = getDay(slot);
        const pNo = getPeriodNo(slot);
        const hasDay = !teacher.uygunGunler || teacher.uygunGunler.length === 0 || teacher.uygunGunler.includes(day);
        const hasHour = !teacher.uygunSaatler || teacher.uygunSaatler.length === 0 || teacher.uygunSaatler.includes(String(pNo));
        return hasDay && hasHour;
      });
    }
    effectiveAvailablePeriods.set(teacher.id, available);
  });

  return {
    plans,
    teachers,
    classes,
    classrooms,
    courses,
    config: finalConfig,
    effectiveAllowedPeriods,
    effectiveAvailablePeriods
  };
}

export function countAllowedSlotsForClass(
  cls: ClassUnit,
  finalConfig: SchoolScheduleConfig,
  effectiveAllowedPeriods?: Map<string, string[]>
): number {
  if (cls.uygunPeriyotlar && cls.uygunPeriyotlar.length > 0) {
    return cls.uygunPeriyotlar.length;
  }

  let allowed = 0;
  finalConfig.activeDays.forEach((day) => {
    const activeCount = getActivePeriodsCountForDay(day, finalConfig);

    for (let periodNo = 1; periodNo <= activeCount; periodNo++) {
      if (finalConfig.ogleArasiPeriyotNo !== undefined && periodNo === finalConfig.ogleArasiPeriyotNo) continue;

      if (!isSlotAllowedForClassPolicy(cls, day, periodNo, activeCount)) continue;

      allowed++;
    }
  });

  return allowed;
}

function calculateProjectedTeacherGapsFromIndex(
  index: ScheduleIndex,
  teacherId: string,
  day: string,
  config: SchoolScheduleConfig
): number {
  const periods = [...(index.teacherDayPeriods.get(indexKey(teacherId, day)) || new Set<number>())]
    .filter(Number.isFinite)
    .sort((a, b) => a - b);

  let totalGaps = 0;
  for (let i = 1; i < periods.length; i++) {
    totalGaps += calculateGap(periods[i - 1], periods[i], config);
  }

  return totalGaps;
}

export interface CandidateDraft {
  score: number;
  slots: string[];
  teacher: Teacher;
  room: Classroom;
}

export function scoreBlockPlacement(
  index: ScheduleIndex,
  teacher: Teacher,
  cls: ClassUnit,
  room: Classroom,
  day: string,
  lastSlot: string,
  finalConfig: SchoolScheduleConfig
): number {
  const teacherWeeklyLoad = index.teacherWeekly.get(teacher.id) || 0;
  const teacherDailyLoad = index.teacherDaily.get(indexKey(teacher.id, day)) || 0;
  const idealDaily = teacher.idealGunlukDers || 6;
  const weeklyLimit = teacher.haftalikMaksimumDers || Math.max(teacherWeeklyLoad, 1);
  const capacityWaste = Math.max(0, room.kapasite - cls.mevcutOgrenciSayisi);
  const gaps = calculateProjectedTeacherGapsFromIndex(index, teacher.id, day, finalConfig);

  let score = 0;
  // Kademeli (quadratic) yük cezası: Yükü fazla olan öğretmenlere ders verilmesini daha agresif şekilde engeller
  score += Math.pow(teacherWeeklyLoad / weeklyLimit, 2) * 45;
  score += Math.max(0, teacherDailyLoad - idealDaily) * 10;
  score += gaps * 4;
  score += capacityWaste / 10;

  const activeCount = getActivePeriodsCountForDay(day, finalConfig);
  const levelViolation = getLevelWindowViolation(cls, day, getPeriodNo(lastSlot), activeCount);
  if (levelViolation?.seviye === "Esnek") score += 25;

  return score;
}

