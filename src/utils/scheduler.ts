export function buildStartSlotCache(
  daysList: string[],
  finalConfig: SchoolScheduleConfig,
  maxBlockHours: number
): Map<number, string[]> {
  const cache = new Map<number, string[]>();

  for (let blockHours = 1; blockHours <= maxBlockHours; blockHours++) {
    const slots: string[] = [];

    daysList.forEach((day) => {
      const activeCount = getActivePeriodsCountForDay(day, finalConfig);

      for (let startPeriodNo = 1; startPeriodNo <= activeCount; startPeriodNo++) {
        if (finalConfig.ogleArasiPeriyotNo !== undefined && startPeriodNo === finalConfig.ogleArasiPeriyotNo) continue;

        const blockSlots = buildBlockSlots(day, startPeriodNo, blockHours, finalConfig);
        if (blockSlots) slots.push(`${day}-${startPeriodNo}`);
      }
    });

    cache.set(blockHours, slots);
  }

  return cache;
}

export function createMulberry32(seed: number): () => number {
  let s = seed;
  return function() {
    let t = s += 0x6D2B79F5;
    t = Math.imul(t ^ (t >>> 15), t | 1);
    t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

export function shuffleInPlace<T>(items: T[], rng?: () => number): T[] {
  const randomFunc = rng || Math.random;
  for (let i = items.length - 1; i > 0; i--) {
    const j = Math.floor(randomFunc() * (i + 1));
    [items[i], items[j]] = [items[j], items[i]];
  }
  return items;
}

export function keepBestCandidates<T extends { score: number }>(candidates: T[], candidate: T, maxSize = 8): void {
  candidates.push(candidate);
  candidates.sort((a, b) => a.score - b.score);
  if (candidates.length > maxSize) candidates.length = maxSize;
}

function pickCandidate<T>(candidates: T[], attempt: number): T | undefined {
  if (candidates.length === 0) return undefined;
  if (attempt === 0) return candidates[0];

  const topWindow = Math.min(candidates.length, 3);
  return candidates[Math.floor(Math.random() * topWindow)];
}

export function diagnoseUnassignedRequirement(
  sinif: ClassUnit,
  course: Course,
  teachers: Teacher[],
  classrooms: Classroom[],
  index: ScheduleIndex,
  finalConfig: SchoolScheduleConfig
): string[] {
  const reasons: string[] = [];

  const compatibleTeachers = teachers.filter(
    (teacher) =>
      isActiveEntity(teacher) &&
      canTeacherTeachCourse(teacher, course) &&
      isTeacherCadreCompatible(teacher, sinif, course)
  );

  if (compatibleTeachers.length === 0) {
    reasons.push("Öğretmen Kilidi: Dersi verebilecek aktif ve kademe uyumlu öğretmen tanımlanmamış.");
  } else if (compatibleTeachers.every((teacher) => (index.teacherWeekly.get(teacher.id) || 0) >= (teacher.haftalikMaksimumDers ?? Number.POSITIVE_INFINITY))) {
    reasons.push("Öğretmen Kilidi: Uygun öğretmenlerin tamamının haftalık maksimum ders yükü limiti dolu.");
  }

  const compatibleRooms = classrooms.filter(
    (room) =>
      isActiveEntity(room) &&
      room.kapasite >= sinif.mevcutOgrenciSayisi &&
      (!room.hizmetBirimi || room.hizmetBirimi === "Tümü" || room.hizmetBirimi === sinif.kademe)
  );
  if (compatibleRooms.length === 0) {
    reasons.push("Derslik Kilidi: Sınıf mevcuduna uygun kapasitede aktif derslik bulunamadı.");
  }

  // Get class allowed slots using override logic
  let allowedSlots: string[] = [];
  if (sinif.uygunPeriyotlar && sinif.uygunPeriyotlar.length > 0) {
    allowedSlots = [...sinif.uygunPeriyotlar];
  } else {
    finalConfig.activeDays.forEach((day) => {
      const activeCount = getActivePeriodsCountForDay(day, finalConfig);
      for (let periodNo = 1; periodNo <= activeCount; periodNo++) {
        if (finalConfig.ogleArasiPeriyotNo !== undefined && periodNo === finalConfig.ogleArasiPeriyotNo) continue;
        if (!isSlotAllowedForClassPolicy(sinif, day, periodNo, activeCount)) continue;
        allowedSlots.push(`${day}-${periodNo}`);
      }
    });
  }

  const occupiedCount = allowedSlots.filter(slot => index.classSlot.has(indexKey(sinif.id, slot))).length;
  const freeSlots = allowedSlots.filter(slot => !index.classSlot.has(indexKey(sinif.id, slot)));
  const freeCount = freeSlots.length;

  if (allowedSlots.length === 0) {
    reasons.push("Fiziksel Pencere Açığı: Sınıfın seviye/pencere kısıtlarına göre tanımlı izinli periyodu yok.");
  } else if (freeCount === 0) {
    reasons.push("Fiziksel Pencere Açığı: Sınıfın izinli zaman penceresindeki tüm periyotlar dolu.");
  }

  if (reasons.length === 0) {
    let teacherBusyInAllFreeSlots = true;
    let roomBusyInAllFreeSlots = true;

    for (const slot of freeSlots) {
      const anyTeacherFree = compatibleTeachers.some(t => {
        const isWeeklyFull = (index.teacherWeekly.get(t.id) || 0) >= (t.haftalikMaksimumDers ?? Number.POSITIVE_INFINITY);
        const isSlotBusy = index.teacherSlot.has(indexKey(t.id, slot));
        
        let teacherAllows = true;
        if (index.effectiveAvailablePeriods && index.effectiveAvailablePeriods.has(t.id)) {
          teacherAllows = index.effectiveAvailablePeriods.get(t.id)!.includes(slot);
        } else {
          const day = getDay(slot);
          const pNo = getPeriodNo(slot);
          teacherAllows = isTeacherAvailable(t, day, pNo, slot);
        }
        
        return !isWeeklyFull && !isSlotBusy && teacherAllows;
      });

      if (anyTeacherFree) {
        teacherBusyInAllFreeSlots = false;
      }

      const anyRoomFree = compatibleRooms.some(r => !index.roomSlot.has(indexKey(r.id, slot)));
      if (anyRoomFree) {
        roomBusyInAllFreeSlots = false;
      }
    }

    if (teacherBusyInAllFreeSlots) {
      reasons.push("Öğretmen Kilidi: Kalan sınıf boş periyotlarında dersi verebilecek öğretmenlerin programı dolu veya uygun değil.");
    }
    if (roomBusyInAllFreeSlots) {
      reasons.push("Derslik Kilidi: Kalan sınıf boş periyotlarında uygun kapasitedeki tüm derslikler dolu.");
    }
  }

  if (reasons.length === 0) {
    reasons.push("Kombinasyon Kilidi: Sınıfta boş yer, öğretmende boş yer ve uygun derslik arasında ortak bir kesişim kümesi yok (Spatiotemporal kilit).");
  }

  return reasons;
}
